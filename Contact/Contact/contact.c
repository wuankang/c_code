#define _CRT_SECURE_NO_WARNINGS 1
#include "contact.h"

void InitContact(struct Contact*pc)
{
	pc->sz = 0;
	//memset设置内存
	memset(pc->data, 0, sizeof(pc->data));//每个字节初始化为0
}
//增加一个人的信息
void AddContact(struct Contact*pc)
{
	//判断通讯录满不满
	if (pc->sz == MAX)
	{
		printf("通讯录已满，无法添加\n");
	}
	else
	{
		printf("请输入名字:->");
		scanf("%s", pc->data[pc->sz].name);
		printf("请输入年龄:->");
		scanf("%d", &(pc->data[pc->sz].age));
		printf("请输入性别:->");
		scanf("%s", pc->data[pc->sz].sex);
		printf("请输入号码:->");
		scanf("%s", pc->data[pc->sz].tel);
		printf("请输入地址:->");
		scanf("%s", pc->data[pc->sz].addr);
		pc->sz++;
		printf("添加成功\n");
	}
}
static int FindByName(const struct Contact*pc,char name[])
{
	int i = 0;
	for (i = 0; i < pc->sz; i++)
	{
		if (strcmp(pc->data[i].name, name) == 0)
		{
			return i;
		}
	}
	return -1;
}
void ShowContact(const struct Contact*pc)
{
	int i = 0;
	if (pc->sz == 0)
	{
		printf("通讯录为空\n");
	}
	else
		printf("%-15s\t%-4s\t%-5s\t%-12s\t%-20s\n", "名字", "年龄", "性别", "电话", "地址");
	{
		for (i = 0; i < pc->sz; i++)
		{
			printf("%-15s\t%-4d\t%-5s\t%-12s\t%-20s\n",
				pc->data[i].name,
				pc->data[i].age,
				pc->data[i].sex,
				pc->data[i].tel,
				pc->data[i].addr);
		}
	}
	
}
void DelContact(struct Contact*pc)
{
	char name[NAME_MAX] = { 0 };
	if (pc->sz == 0)
	{
		printf("通讯录为空，没法删除\n");
	}
	else
	{
		//1.找到要删除的人
		printf("请输入删除的人名：->");
		scanf("%s", name);
		int pos=FindByName(pc, name);
		if (pos == -1)
		{
			printf("要删除的人不存在\n");
		}
		else
		{
			int j = 0;
			for (j = pos; j < pc->sz-1; j++)
			{
				pc->data[j] = pc->data[j + 1];
			}
			//2.删除
			pc->sz--;
			printf("删除成功\n");
		}
	}
	
}
void SearchContact(const struct Contact*pc)
{
	char name[NAME_MAX];
	printf("请输入要查找的名字：->");
	scanf("%s", name);
	int pos = FindByName(pc, name);
	if (pos == -1)
	{
		printf("要查找的人不存在\n");
	}
	else
	{
		printf("%-15s\t%-4s\t%-5s\t%-12s\t%-20s\n", "名字", "年龄", "性别", "电话", "地址");
		printf("%-15s\t%-4d\t%-5s\t%-12s\t%-20s\n",
			pc->data[pos].name,
			pc->data[pos].age,
			pc->data[pos].sex,
			pc->data[pos].tel,
			pc->data[pos].addr);
	}
}
